    private void btnCargarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarPartidaActionPerformed
    String nombre = txtNombreJugador.getText().trim();
    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el nombre del jugador que desea cargar.");
        return;
    }

    String archivo = "partida_" + nombre + ".json";
    File f = new File(archivo);

    if (!f.exists()) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è No existe una partida guardada con ese nombre.");
        return;
    }

    Jugador jugador = GestorJSON.cargarJugador(archivo);
    Army[][] mapa = GestorJSON.cargarMapa(archivo, 25);

    VentanaJuego ventanaJuego = new VentanaJuego(jugador, mapa);
    ventanaJuego.setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnCargarPartidaActionPerformed

